package com.fdmgroup.exercises.threelittlepigs;

public class House 
{
	private int houseNum;	

	public void buildHouse()
	{
		System.out.println("Finished Building House"+houseNum);
	}
	
	public void blowOnHouse()
	{
		System.out.println("Blow on house "+houseNum);
	}
	
	public void fallDown()
	{
		System.out.println("House "+houseNum+" was blown down");
	}
	
	public int getHouseNum() {
		return houseNum;
	}

	public void setHouseNum(int houseNum) {
		this.houseNum = houseNum;
	}
	
	public void incrementHouse()
	{
		houseNum++;
	}
}
